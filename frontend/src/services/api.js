import axios from 'axios';
import { getToken } from '../utils/auth';

const API_BASE_URL = process.env.REACT_APP_API_URL || 'http://localhost:8080';

const apiClient = axios.create({
  baseURL: API_BASE_URL,
  headers: {
    'Content-Type': 'application/json',
  },
});

apiClient.interceptors.request.use(
  (config) => {
    const token = getToken();
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    return config;
  },
  (error) => Promise.reject(error)
);

apiClient.interceptors.response.use(
  (response) => response,
  (error) => {
    if (error.response && error.response.status === 401) {
      localStorage.removeItem('token');
      window.location.href = '/login';
    }
    return Promise.reject(error);
  }
);

export const productApi = {
  getAll: (page = 0, size = 12) =>
    apiClient.get(`/api/products?page=${page}&size=${size}`),
  getById: (id) => apiClient.get(`/api/products/${id}`),
  search: (keyword) => apiClient.get(`/api/products/search?keyword=${keyword}`),
  getByCategory: (categoryId) =>
    apiClient.get(`/api/products/category/${categoryId}`),
};

export const orderApi = {
  create: (order) => apiClient.post('/api/orders', order),
  getById: (id) => apiClient.get(`/api/orders/${id}`),
  getMyOrders: (userId) => apiClient.get(`/api/orders/user/${userId}`),
  cancel: (id) => apiClient.post(`/api/orders/${id}/cancel`),
};

export const authApi = {
  login: (credentials) => apiClient.post('/api/auth/login', credentials),
  register: (userData) => apiClient.post('/api/auth/register', userData),
};

export default apiClient;


/**
 * Formats a date string for display purposes.
 * @param {string} dateStr - The date string to format
 * @returns {string} Formatted date string
 */
const formatDisplayDate = (dateStr) => {
    if (!dateStr) return '';
    const date = new Date(dateStr);
    return date.toLocaleDateString('vi-VN', {
        year: 'numeric',
        month: '2-digit',
        day: '2-digit'
    });
};



/**
 * Debounce function to limit rapid invocations.
 * @param {Function} func - The function to debounce
 * @param {number} wait - Delay in milliseconds
 * @returns {Function} Debounced function
 */
const debounce = (func, wait = 300) => {
    let timeout;
    return (...args) => {
        clearTimeout(timeout);
        timeout = setTimeout(() => func.apply(this, args), wait);
    };
};



